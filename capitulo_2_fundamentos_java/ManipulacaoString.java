public class ManipulacaoString {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;

        System.out.println("O resultado é " + x + y);
        //resultado 105

        System.out.println("O resultado é " + (x + y));
        //resultado 15

        System.out.println(x + y + " é o resultado.");
        // 15 é o resultado
    }
}